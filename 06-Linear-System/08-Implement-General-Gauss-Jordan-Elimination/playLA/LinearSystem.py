    #    / /   \  /
    #   / /     \/
    #  / /      /\
    # / /      /  \
    #
    # R1-c: 方程个数 > 未知数个数: 无解 / 唯一解 / 无数解
    # 如下 3 个方程 > 2 个未知数 (2 维):
    #    / / /     \/ /   \ | /
    #   / / /      /\/     \|/
    #  / / /      / /\     /|\
    # / / /      / /  \   / | \
    #
    #
    # 结论二 (R2)
    # R2-a: 行最简形式 matrix 非零行 < 行最简形式 augmented_matrix 非零行: 无解
    # [ 1 0 0 | c1 ]
    # [ 0 1 0 | c2 ]
    # [ 0 1 0 | c3 ]
    # [ 0 0 0 | c4 ]
    #
