    # [ 1 0 0 0 | c1 ]
    # [ 0 0 1 0 | c2 ]
    # [ 0 0 0 1 | c3 ]
    # [ 0 0 0 0 | 0  ]
    #
    # R2-c: 行最简形式 matrix 非零行 = 未知数个数: 唯一解
    # [ 1 0 0 | c1 ]
    # [ 0 1 0 | c2 ]
    # [ 0 0 1 | c3 ]
    # [ 0 0 0 | 0  ]
    #
